def patient_forgot_email_view(request):
    try:
        if request.method == 'POST':
            username = request.POST.get('username')
            
            if not models.User.objects.filter(username=username).first():
                messages.error(request, 'Not user found with this username.')
                return redirect('patient-forgot-email')
            
            user_obj = models.User.objects.get(username = username)
            token = str(uuid.uuid4())
            # profile_obj = models.Profile(user = user_obj,forget_password_token=token)
            profile_obj= models.Profile.objects.get(user = user_obj)
            profile_obj.forget_password_token = token
            profile_obj.save()
            send_forget_password_mail(user_obj.email , token)
            messages.success(request, 'An email is sent.')
            return redirect('patient-forgot-email')                  
    except Exception as e:
        print(e)
    return render(request , 'hospital\patient_forgot_emai.html')


def patient_enter_password_view(request , token):
    context = {}   
    try:
        profile_obj = models.Profile.objects.filter(forget_password_token = token).first()
        context = {'user_id' : profile_obj.user.id}
        
        if request.method == 'POST':
            new_password = request.POST.get('new_password')
            confirm_password = request.POST.get('reconfirm_password')
            user_id = request.POST.get('user_id')
            
            if user_id is  None:
                messages.success(request, 'No user id found.')
                return redirect(f'patient-enter-password/{token}/')
                           
            if  new_password != confirm_password:
                messages.success(request, 'both should  be equal.')
                return redirect(f'patient-enter-password/{token}/')
                                    
            user_obj = models.User.objects.get(id = user_id)
            user_obj.set_password(new_password)
            user_obj.save()
            return redirect(reverse('patientlogin'))      
    except Exception as e:
        print(e)
    return render(request , 'hospital\patient_re_enter_psd.html' , context)




