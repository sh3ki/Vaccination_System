@login_required
def patient_vaccine_tracking(request):
    today = datetime.date.today()
    user = request.user
    patient = models.Patient.objects.get(user=user)
    vaccine_schedule = patient.save_vaccine_schedule(user)
    vaccine_schedules = [vaccine_schedule]  # Create a list with the single vaccine schedule for the patient
    vaccine_info = models.VaccineInfo.objects.all()
    vaccine_infos = models.VaccineInfo.objects.all().order_by('to_be_given')
    appointments=models.Appointment.objects.all()

    grouped_data = {}
    for info in vaccine_infos:
        if info.to_be_given in grouped_data:
            grouped_data[info.to_be_given].append(info)
        else:
            grouped_data[info.to_be_given] = [info]
    # print(grouped_data)
     # Custom sorting function for 'to_be_given' values
    def custom_sort(to_be_given):
        order = {
            'BIRTH': 0,
            '6 WEEKS': 1,
            '10 WEEKS': 2,
            '14 WEEKS': 3,
            '6 MONTHS': 4,
            '9 MONTHS': 5,
            '9-12 MONTHS': 6,
            '12 MONTHS': 7,
            '15 MONTHS': 8,
            '16-18 MONTHS': 9,
            '18 MONTHS': 10,
            '2 YEARS': 11,
            '4-6 YEARS': 12,
            '10-12 YEARS': 13,
        }
        return order.get(to_be_given, 999)  # Default to 999 if value not found in order dictionary

    grouped_data = dict(sorted(grouped_data.items(), key=lambda x: custom_sort(x[0]), reverse=False))
    # grouped_data = dict(sorted(grouped_data.items(), reverse=True))
    context = {
        'grouped_data': grouped_data,
        'vaccine_schedules': vaccine_schedules,
        'vaccine_infos':vaccine_info,
        'appointments':appointments,
        'today':today,
        'patient':patient
    }

    return render(request, 'hospital/patient_vaccine_tracking.html', context)

